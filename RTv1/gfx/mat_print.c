#include "gfx.h"
#include <stdio.h>

void	mat_print(float mat[4][4])
{
	int		y;
	int		x;

	y = 0;
	x = 0;
	while (y < 4)
	{
		while (x < 4)
		{
			printf("%f ", mat[y][x]);
			x++;
		}
		printf("\n");
		x = 0;
		y++;
	}
	printf("\n");
}
