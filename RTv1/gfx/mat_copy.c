#include "gfx.h"

void	mat_copy(float src[4][4], float dst[4][4])
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			dst[i][j] = src[i][j];
			j++;
		}
		i++;
		j = 0;
	}
}
